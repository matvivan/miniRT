#include "libft.h"

char		*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	char	*begin;

	i = 0;
	begin = s1;
	while (*s1 != '\0')
		s1++;
	while (s2[i] != '\0' && i < n)
		*s1++ = s2[i++];
	*s1 = '\0';
	return (begin);
}
